package Day1.JavaAssignments;

public class Characters {
    public static void main(String[] args) {
        System.out.printf("A=%d B=%d C=%d%n", (int)'A', (int)'B', (int)'C');
        System.out.printf("a=%d b=%d c=%d%n", (int)'a', (int)'b', (int)'c');
        System.out.printf("0=%d 1=%d 2=%d%n", (int)'0', (int)'1', (int)'2');
        System.out.printf("+=%d /=%d (blank)=%d%n", (int)'+', (int)'/', (int)' ');
    }
}